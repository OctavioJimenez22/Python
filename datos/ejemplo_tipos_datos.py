#Ejemplo de suma de valores

numero1 = "10"
print(f"Valor 1 en cadena: {numero1}")
numero2 = "20"
print(f"valor 2 en cadena: {numero2}")
resultado= numero1 + numero2
print(f"Como es suma de cadenas aparece asi: {resultado}\n")

#convertir a tipo entero
numero1_entero = int(numero1)
numero2_entero = int(numero2)
resultado = numero1_entero + numero2_entero
print(f"Suma de valores convertidos a entero: {resultado}")